// while loop
//  is used when the number of iterations is not know in advance 

let idx = 0;
while (idx <= 10) {
    // console.log(`value ${idx}`);
    idx++
}


// print array using while loop

let myarr = [1, 2, 3, 4, 5]
let arr = 0;

while (arr < myarr.length) {
    console.log(`value ${myarr[arr]}`);
    arr++

}